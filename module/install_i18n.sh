INSTALL_LANG="en"
_detect_lang() {
    local raw
    raw=$(getprop ro.system.locale 2>/dev/null)
    [ -z "$raw" ] && raw=$(getprop persist.sys.locale 2>/dev/null)
    [ -z "$raw" ] && raw=$(getprop ro.product.locale 2>/dev/null)
    local code=$(printf '%s' "$raw" | sed 's/_/-/g')
    case "$code" in
        zh-Hans*|zh-CN*) code="zh-CN" ;;
        zh-Hant*|zh-TW*) code="zh-TW" ;;
        pt-BR*) code="pt-BR" ;;
        pt*) code="pt-PT" ;;
        *-*) code="${code%%-*}" ;;
    esac
    case "$code" in
        af|ar|bg|bn|ca|cs|da|de|el|en|es|fa|fi|fr|he|hi|hu|id|it|ja|ko|nl|no|pl|pt-BR|pt-PT|ro|ru|sr|sv|th|tr|uk|vi|zh-CN|zh-TW) INSTALL_LANG="$code" ;;
    esac
}
